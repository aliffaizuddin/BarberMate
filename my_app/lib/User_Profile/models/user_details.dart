class UserProfile {
  final String name;
  final String role;
  final String location;
  final int starRating;
  final String phoneNumber;
  final String profilePicture;

  UserProfile({
    required this.name,
    required this.role,
    required this.location,
    required this.starRating,
    required this.phoneNumber,
    required this.profilePicture,
  });
}
